import UIKit

extension UIColor {
    public class func _darkColor() -> UIColor {
        return UIColor(hex: "#262528")
    }
    
    public class func _orangeColor() -> UIColor {
        return UIColor(hex: "#EC6C26")
    }
    
    public class func _baseColor() -> UIColor {
        return UIColor(hex: "#007AFF")
    }
}